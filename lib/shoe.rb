class Shoe
  def initialize(brand, color, size, material, condition)
    @brand = brand 
  end 
   
  def brand=(brand)
    @brand = brand 
  end 
  
  def brand
    @brand 
  end 
  
  def color=(color)
    @color = color 
  end 
  
  def color 
    @color 
  
  def size=(size)
    @size = size  
  end 
  
  def size 
    @size 
  
  def material=(material)
    @material = material 
  end 
  
  def material 
    @material 
  
  def condition=(condition)
    @condition = condition 
  end 
  
  def condition
    @condition
  end 
end 